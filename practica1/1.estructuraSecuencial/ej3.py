# 3. Dados como datos cinco números obtener el promedio de los mismos e informar el resultado.

# Pide los 5 numeros
n1 = int(input("Ingrese el numero 1: "))
n2 = int(input("Ingrese el numero 2: "))
n3 = int(input("Ingrese el numero 3: "))
n4 = int(input("Ingrese el numero 4: "))
n5 = int(input("Ingrese el numero 5: "))

#Calcula el promedio
promedio = (n1 + n2 + n3 + n4 + n5)/5

# Muestra el promedio
print ("El promedio de", n1, ",", n2, ",", n3, ",", n4, ",", n5, "=", promedio)


